package com.example.pizza.enam;

/**
 * The PaymentStatus enum represents the status of a payment.
 */
public enum PaymentStatus {

    /**
     * Indicates that the payment is pending.
     */
    PENDING,

    /**
     * Indicates that the payment was successful.
     */
    SUCCESSFUL,

    /**
     * Indicates that the payment has failed.
     */
    FAILED
}

